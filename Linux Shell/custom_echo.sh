# Write a Bash script which accepts name as input and displays a greeting: "Welcome (name)"

# Input Format:

# One line, containing a .

# Output Format

# One line: "Welcome (name)" (quotation marks excluded).
# The evaluation will be case-sensitive.

# Sample Input:
# Dan 
 
# Sample Output:
# Welcome Dan

#---------------------------------------------------------------------

# Main Code:

read name
echo Welcome $name

#---------------------------------------------------------------------